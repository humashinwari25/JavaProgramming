package day03_EscapeSequences;
/*
escape sequences: MUST be given with double quote
\n: starts a new line
\t: paragragraph space =  tab
\\:single back slash
\": double quote
 */
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#");

        System.out.println("-----------------------------------------------------");

        System.out.println("Hello Cydeo! \nHow are you all today? \nIt's nice to see you all! \nWhat class do we have next week?");

        System.out.println("-----------------------------------------------------");

        System.out.println("\tJava is a cool Programming Language");

        System.out.println("-----------------------------------------------------");

        System.out.println("/ \\"); //in order to print one backward slash we need to give two backward slashes

        System.out.println("-----------------------------------------------------");

        System.out.println("My favorite TV show is \"Game of Throne\"");

    }
/*any time in java double quote("") is used for string of text
to give double quote to something use forward slash and quote.. ex: /"
*/




}
